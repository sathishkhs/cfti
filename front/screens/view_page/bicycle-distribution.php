            <p class="text-center mt-3">On 26th and 27th April 2022, we distributed 500 bicycles to underprivileged girl
students. These girls are from remote villages in coastal Maharashtra, to name a
few are Andoshi, Ambepur, Usar, Varande, Devghar, Sarai, Usroli, Murud,
Walvati, Khardodukle, Kharikwadi, and Wadghar. Everyday girls from these
villages walk over 3 kms to reach school. They belong to the fishermen, farmer
and tribal communities. The bicycles they received will ease their commute to
school, help them to continue their education and will reduce drop-out rate.</p>

