        <div class="col-12">
            <p class="text-center mt-3">On 14th &amp; 15th May 2022, we distributed 100 bicycles in Vavdungi, Telawade,
Shighre, Undergaon, Josranjan &amp; Amboli Bauddhawada in Murud Taluka. The
bicycles were given to girls from underprivileged backgrounds whose parents
cannot afford to give them bicycles or money to commute. Every day, these girls
walk for more than 4 kms to reach school. The bicycles that were given to them
will ease their commute to school and also facilitate continuing their education.</p>

